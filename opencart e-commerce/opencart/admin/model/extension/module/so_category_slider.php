	function recurseTree($cat, $level, $all_cats, &$categories) {
		$probil = '';
		for ($i = 0; $i < $level; $i++) {
			$probil .= '-- ';
		}
		$cat['level'] = $level;
		$cat['name'] = ($probil . $cat['name']);
		$categories[] = $cat;
		foreach ($all_cats as $categ) {
			if($categ['parent_id'] == $cat['category_id']) {
				self::recurseTree($categ, ++$level, $all_cats, $categories);
				$level--;
			}
		}
		return $categories;
	}
	
